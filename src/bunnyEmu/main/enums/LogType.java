package bunnyEmu.main.enums;

/**
 * This enum defines all log types.
 * @author Valkryst
 * --- Last Edit 16-June-2014
 */
public enum LogType {
	VERBOSE, // Explanations. (Ex: Regular logs.)
	WARNING, // Warnings. (Ex: An exception was thrown, but nothing will crash.)
	ERROR; // Errors. (Ex: Program is going to crash)
}
